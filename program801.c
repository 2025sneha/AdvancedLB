void Encrypt(
                char *str
            )
{
    while (*str != '\0')
    {
        *str = *str + 1;
        str++;
    }
}

//////////////////////////////////////////////////////////////////////////////////////
//
//   Entry point function (main)
//
////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = {'\0'};

    printf("Enter the data : \n");
    scanf("%[^'\n']s",Arr);

    Encrypt(Arr);

    printf("Encrypted data : %s\n",Arr);
    
    return 0;
}